    private GregorianCalendar gc=new GregorianCalendar();
    private Hashtable<String, Cita> citas=new Hashtable<String, Cita>();
    private AgendaListModel alm=new AgendaListModel();
    
    private void update(){
        String sMeses[]={"ENE", "FEB", "MAR", "ABR", "MAY", "JUN", "JUL", "AGO", "SEP", "OCT", "NOV", "DIC"};
        int ano=gc.get(GregorianCalendar.YEAR);
        int nMes=gc.get(GregorianCalendar.MONTH);
        int nDia=gc.get(GregorianCalendar.DATE);
        jLabel1.setText(nDia + "/" + sMeses[nMes] + "/" + ano);
        jTextField1.setText(""+ano);
        jTextField2.setText(""+(nMes+1));
        jTextField3.setText(""+nDia);
        
        jLabel6.setText("Citas:" + citas.size());
        
        alm.limpiar();
        Enumeration<Cita> elementos=citas.elements();
        while (elementos.hasMoreElements()){
            Cita c=elementos.nextElement();
            GregorianCalendar g=c.getGc();
            if (g.get(GregorianCalendar.YEAR) == gc.get(GregorianCalendar.YEAR) &&
                g.get(GregorianCalendar.MONTH) == gc.get(GregorianCalendar.MONTH) &&
                g.get(GregorianCalendar.DATE) == gc.get(GregorianCalendar.DATE) ){
                alm.addCita(c);
            }
        }
        alm.ordenarHora();
        jList1.setModel(alm);
        jList1.setCellRenderer(new DefaultListCellRenderer());
    }
    
    private void incDia(int n){
        int ano=gc.get(GregorianCalendar.YEAR);
        int nMes=gc.get(GregorianCalendar.MONTH);
        int nDia=gc.get(GregorianCalendar.DATE)+n;
        gc.set(ano, nMes, nDia);
    }
    
