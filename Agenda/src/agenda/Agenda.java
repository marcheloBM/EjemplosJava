    private boolean esExtensionAgenda(File f){
        if (f.length()>3){
            String sNombre=f.getAbsolutePath().toLowerCase();
            if (sNombre.substring(sNombre.length()-4,sNombre.length()).equals(".agn")){
                return true;
            }
        }
        return false;
    }

    private FileFilter getFileFilter(){
        return new FileFilter() {
            public boolean accept(File f) {
                if (f.isDirectory())
                    return true;
                return esExtensionAgenda(f);
            }
            public String getDescription() {
                return "Ficheros agenda Solo Programadores";
            }
        };
    }

    
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
// TODO add your handling code here:
// TODO add your handling code here:
        JFileChooser jfc=new JFileChooser();
        jfc.setFileFilter(getFileFilter());
        int nResult = jfc.showOpenDialog(this);
        if (nResult == JFileChooser.APPROVE_OPTION){
            try{
                File f = jfc.getSelectedFile();
                ObjectInputStream ois=new ObjectInputStream(new FileInputStream(f));
                citas = (Hashtable)ois.readObject();
                ois.close();
                update();
            }catch(Exception e){
                e.printStackTrace();
            }
        }        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
// TODO add your handling code here:
        JFileChooser jfc=new JFileChooser();
        jfc.setFileFilter(getFileFilter());
        int nResult = jfc.showSaveDialog(this);
        if (nResult == JFileChooser.APPROVE_OPTION){
            try{
                File f = jfc.getSelectedFile();
                ObjectOutputStream oos=new ObjectOutputStream(new FileOutputStream(f));
                oos.writeObject(citas);
                oos.flush();
                oos.close();
            }catch(Exception e){
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

